</div><button class="collapsible">O365 General Information</button>
<div class="contentCollapsible">
  <p><p><strong><span data-contrast="auto">What is O365?</span></strong></p>
<p><span data-contrast="auto">Office 365 is an integrated experience of apps and services all in one place, that provides a suite of new productivity and business tools, which you can access from anywhere on any device.</span><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span><span data-contrast="auto">David Conway explains what Office 365 is and what benefits it&nbsp;opens up&nbsp;in&nbsp;</span><a href="https://www.sunderland.ac.uk/digital-first/"><span data-contrast="none">this short video</span></a><span data-contrast="auto">&nbsp;for Digital First.</span><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span></p>
<p><strong><span data-contrast="auto"><br>Why are we doing this?</span></strong></p>
<p><span data-contrast="auto">There are several&nbsp;</span><span data-contrast="auto">benefits</span><span data-contrast="auto">&nbsp;</span><span data-contrast="auto">to</span><span data-contrast="auto">&nbsp;rolling out Office 365:</span><span data-ccp-props="{&quot;134233117&quot;:true,&quot;134233118&quot;:true,&quot;201341983&quot;:0,&quot;335559740&quot;:240}">&nbsp;</span></p>
<ul>
<li><span data-contrast="auto">Greater ease of collaboration</span>
<ul>
<li><span data-contrast="auto">Microsoft Teams is a new hub for collaboration within your team and across teams</span></li>
<li><span data-contrast="auto">SharePoint makes sharing documents easy and quick – share links to a single document rather than sending email attachments</span></li>
</ul>
</li>
<li><span data-contrast="auto">Improved engagement for teachers and learners</span>
<ul>
<li><span data-contrast="auto">Once students will join Office 365, they can make use of the collaboration tools within Office 365</span></li>
<li><span data-contrast="auto">Students will work with industry-standard tools which will prepare them for their professional careers</span></li>
</ul>
</li>
<li><span data-contrast="auto">Opens opportunities for broader uptake of agile working</span>
<ul>
<li><span data-contrast="auto">Work from any location or device of your choice without losing files or having to copy anything across device</span></li>
</ul>
</li>
</ul>
<p>&nbsp;</p>
<p><strong><span data-contrast="auto">Who is getting&nbsp;</span></strong><strong><span data-contrast="auto">O365</span></strong><strong><span data-contrast="auto">?</span></strong></p>
<p><span data-contrast="auto">Initially, only University staff will have access to O365, with students to follow at a later point in 2020.&nbsp;</span><span data-contrast="auto">All staff will get access to O365 at the same time so everyone can use the collaborative tools in O365 immediately.</span><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;<br></span><span data-contrast="auto">The</span><span data-contrast="auto">re are 2</span><span data-contrast="auto">&nbsp;reasons for students not coming in at the same time&nbsp;</span><span data-contrast="auto">as staff</span><span data-contrast="auto">: to give staff time to get used to Office 365 first and sort out the technology behind it, as some additional work will need to be done first.</span><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span><span data-contrast="auto">This means that staff are advised not to collaborate</span><span data-contrast="auto">&nbsp;with students</span><span data-contrast="auto">&nbsp;via Office 365 until students are on it as well.</span></p>
<p><strong><span data-contrast="auto"><br>What about students?</span></strong></p>
<p>Students are not part of the initial launch in February.</p>
<p><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}"><strong><span data-contrast="auto"><br>When is it coming?</span></strong></span></p>
<p>The launch of the core apps will be in February 2020 for all staff. Students will join later in 2020.</p>
<p><strong><span data-contrast="auto"><br>Will it work for everyone internationally as well?</span></strong><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span></p>
<p><span data-contrast="auto">Office 365 is available in all countries&nbsp;</span><span data-contrast="auto">with the exception of</span><span data-contrast="auto">&nbsp;</span><span data-contrast="auto">Cuba, Iran, Democratic People's Republic of Korea, Sudan, and Syria</span><span data-contrast="auto">. If you are travelling abroad, or are working on a campus abroad, you can still use O365.</span>&nbsp;<br><span data-contrast="auto">See:&nbsp;</span><a href="https://products.office.com/en-gb/business/international-availability"><span data-contrast="none">https://products.office.com/en-gb/business/international-availability</span></a><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span></p>
<p><strong><span data-contrast="auto"><br>How much storage will I have within OneDrive/Teams/SharePoint?</span></strong><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span></p>
<p><span data-contrast="auto">Your storage is going to increase from what you currently have. Your OneDrive storage will be 1TB, each team in&nbsp;</span><span data-contrast="auto">Teams will have</span><span data-contrast="auto">&nbsp;</span><span data-contrast="auto">up to 25</span><span data-contrast="auto">&nbsp;</span><span data-contrast="auto">TB per group</span><span data-contrast="auto">,</span><span data-contrast="auto">&nbsp;and SharePoint will</span><span data-contrast="auto">&nbsp;also have&nbsp;</span><span data-contrast="auto">up to 25 TB available per</span><span data-contrast="auto">&nbsp;site collection.</span><span data-ccp-props="{&quot;201341983&quot;:0,&quot;335559739&quot;:160,&quot;335559740&quot;:259}">&nbsp;</span></p>
